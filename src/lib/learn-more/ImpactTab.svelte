    <p class="mb-6">
        Urban agriculture and local food production can have transformative effects on cities:
    </p>
    <ul class="list-disc pl-6 mb-6">
        <li>Increased food security and resilience</li>
        <li>Reduced carbon footprint from food transportation</li>
        <li>Enhanced community engagement and education</li>
        <li>Improved urban biodiversity</li>
        <li>Economic opportunities for local communities</li>
    </ul>

    <p class="mb-6 font-semibold text-lg">
        Home Grown aims to foster positive transformation by actively addressing and contributing to the following Sustainable Development Goals:
    </p>

    <!-- SDG 2: Zero Hunger -->
    <div class="mb-8 bg-[#DDA63A]/10 p-6 rounded-lg ">
        <h3 class="text-2xl font-bold text-[#DDA63A] mb-3">SDG 2: Zero Hunger</h3>
        <p class="italic mb-3">End hunger, achieve food security and improved nutrition and promote sustainable agriculture</p>
        <ul class="list-disc pl-6 space-y-2">
            <li><strong>Increase Food Security and Access:</strong> Urban agriculture can provide a direct, close-to-home source of fresh food, especially for low-income and vulnerable communities, reducing reliance on long and volatile supply chains</li>
            <li><strong>Improve Nutrition:</strong> Home and rooftop gardens typically focus on high-value, nutrient-rich crops like fresh fruits and vegetables, which are often lacking in urban diets. Consuming food shortly after harvest also retains maximum nutritional value.</li>
        </ul>
    </div>

    <!-- SDG 3: Good Health and Well-being -->
    <div class="mb-8 bg-[#4C9F38]/10 p-6 rounded-lg ">
        <h3 class="text-2xl font-bold text-[#4C9F38] mb-3">SDG 3: Good Health and Well-being</h3>
        <p class="italic mb-3">Ensure healthy lives and promote well-being for all at all ages</p>
        <ul class="list-disc pl-6 space-y-2">
            <li><strong>Promote better Physical Health:</strong> Growing and consuming fresh, organic, self-produced food leads to a more nutritious diet and reduces the risk of diet-related illnesses.</li>
            <li><strong>Improve Mental Health:</strong> Engaging with nature, even in a small urban space, is shown to reduce stress, improve mental well-being, and provide a sense of accomplishment and connection.</li>
            <li><strong>Reduce Exposure to Pollution:</strong> Rooftop agriculture can improve air quality in the immediate vicinity and contribute to mitigating the urban heat island effect, leading to a healthier microclimate.</li>
        </ul>
    </div>

    <!-- SDG 10: Reduced Inequalities -->
    <div class="mb-8 bg-[#DD1367]/10 p-6 rounded-lg ">
        <h3 class="text-2xl font-bold text-[#DD1367] mb-3">SDG 10: Reduced Inequalities</h3>
        <p class="italic mb-3">Reduce inequality within and among countries</p>
        <ul class="list-disc pl-6 space-y-2">
            <li><strong>Address Food Inequity:</strong> Urban agriculture in homes and on underutilized rooftops can be a powerful tool to bring healthy, affordable food into low-income communities, directly tackling economic and geographical barriers to good nutrition.</li>
            <li><strong>Empower Vulnerable Groups:</strong> It can provide an income source and valuable skills for vulnerable groups, such as women or refugees, who can sell surplus produce.</li>
            <li><strong>Contribute to Economic Savings:</strong> For households, growing their own food reduces the amount of money spent on groceries, increasing the family budget and lessening economic stress, which is a key factor in reducing poverty and inequality.</li>
        </ul>
    </div>

    <!-- SDG 11: Sustainable Cities and Communities -->
    <div class="mb-8 bg-[#FD9D24]/10 p-6 rounded-lg ">
        <h3 class="text-2xl font-bold text-[#FD9D24] mb-3">SDG 11: Sustainable Cities and Communities</h3>
        <p class="italic mb-3">Make cities and human settlements inclusive, safe, resilient and sustainable</p>
        <ul class="list-disc pl-6 space-y-2">
            <li><strong>Promote Sustainable Use of Land:</strong> Home and rooftop farming utilize unused or underutilized vertical and horizontal spaces (like rooftops and balconies), preventing land-use conflicts and making cities more productive.</li>
            <li><strong>Promote Green Public Spaces:</strong> Community-oriented farms create new green spaces, improving the aesthetics and quality of urban living.</li>
            <li><strong>Increase Climate Change Resilience:</strong> Rooftop gardens can manage stormwater runoff, insulate buildings to reduce energy demand for heating/cooling, and help cool the urban environment, making cities more resilient to climate change impacts.</li>
            <li><strong>Enable Community Cohesion:</strong> Community gardens create opportunities for social interaction and community building, fostering inclusive and participatory urban planning.</li>
        </ul>
    </div>

    <!-- SDG 12: Responsible Consumption and Production -->
    <div class="mb-8 bg-[#BF8B2E]/10 p-6 rounded-lg ">
        <h3 class="text-2xl font-bold text-[#BF8B2E] mb-3">SDG 12: Responsible Consumption and Production</h3>
        <p class="italic mb-3">Ensure sustainable consumption and production patterns</p>
        <ul class="list-disc pl-6 space-y-2">
            <li><strong>Reduce Food Waste:</strong> Growing food at home promotes direct consumption, often of 'ugly' produce that would be rejected by commercial chains. Composting at home also diverts food scraps from landfills.</li>
            <li><strong>Foster Sustainable Resource Management:</strong> Local production significantly reduces associated fossil fuel consumption and carbon emissions from production to distribution.</li>
            <li><strong>Promote Circular Economy:</strong> Urban agriculture integrates into the urban ecosystem by using local waste streams (such as food scraps for compost, and greywater for irrigation) as inputs, promoting a more circular and sustainable production model.</li>
        </ul>
    </div>
