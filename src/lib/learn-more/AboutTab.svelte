    <p class="mb-4">
        HomeGrown is an innovative and interactive tool that is designed to empower urban planners to design climate resilient cities of tomorrow!
    </p>
    <p class="mb-4">
        Having identified 'food security' as the problem we want to focus on within the urban context, we developed a platform that allows users to reimagine their neighborhoods as centres of local food production! The tool allows its users to draw a polygon over any location within their neighbourhood (or around the world!), for example your backyard, an abandoned parking lot, your roof, or even your unutilised balcony to find out the food growing potential of the selected area. Once the area is selected, our platform will provide key environmental and climatic data conditions of the place and provide a reimagination of your selected area.
    </p>
    <p class="mb-4">
        Not only will the tool provide an aggregated data of crop suitability conditions, it will then turn these into useful recommendations for action:
    </p>
    <ol class="list-decimal pl-6 mb-6 space-y-2">
        <li><strong>Which is the most ideal crop to be planted in the selected area?</strong></li>
        <li><strong>When it should be planted?</strong> (based on optimal planting cycles)</li>
        <li><strong>How much yield is to be expected?</strong></li>
        <li><strong>If there is any need for irrigation?</strong></li>
        <li><strong>How it should be planted?</strong> (planting plan)</li>
        <li><strong>How many days you can expect in total you can plant that particular crop?</strong></li>
    </ol>
    <p class="mb-6">
        We believe this easy to use, and informative tool would serve as a powerful tool for decision makers around the world to plan for climate resilient cities that is prepared for the uncertainties of climate change.
    </p>
    <p class="text-lg font-semibold mb-4">
        Feel free to use our platform to explore YOUR neighborhood and start the conversation on how to rethink your balcony or backyard!
